    private void generateFile(String mainMsg) {
        File arquivo = new File("/home/victor/NetBeansProjects/WhatsGUI/src/main/java/com/mycompany/whatsgui/app.js");

        try {
            // Cria o arquivo se ele não existir
            if (arquivo.createNewFile()) {
                System.out.println("Criado!");
            } else {
                System.out.println("O arquivo já existe.");
            }

            // Escreve o conteúdo no arquivo
            try (BufferedWriter writer = new BufferedWriter(new OutputStreamWriter(new FileOutputStream(arquivo), StandardCharsets.UTF_8))) {
                writer.write("const { Client } = require('whatsapp-web.js');");
                writer.newLine();
                writer.write("const qrcode = require('qrcode-terminal');");
                writer.newLine();
                writer.write("");
                writer.newLine();
                writer.write("const client = new Client({");
                writer.newLine();
                writer.write("    puppeteer: {");
                writer.newLine();
                writer.write("        args: ['--no-sandbox', '--disable-setuid-sandbox'],");
                writer.newLine();
                writer.write("    }");
                writer.newLine();
                writer.write("});");
                writer.newLine();
                writer.write("");
                writer.newLine();
                writer.write("client.on('ready', () => {");
                writer.newLine();
                writer.write("    console.log('Client is ready!');");
                writer.newLine();
                writer.write("});");
                writer.newLine();
                writer.write("");
                writer.newLine();
                writer.write("client.on('qr', qr => {");
                writer.newLine();
                writer.write("    qrcode.generate(qr, { small: true });");
                writer.newLine();
                writer.write("});");
                writer.newLine();
                writer.write("");
                writer.newLine();
                writer.write("client.on('message', async message => {");
                writer.newLine();

                // ###########################
                // Aqui você pode continuar a adicionar o restante do seu código
                // writer.write("    // seu código aqui");
                // writer.newLine();
                
                // MAIN MESSAGE
                writer.write("let hello_msg = `");
                writer.newLine();
                writer.write(mainMsg);
                writer.newLine();
                writer.write("`;");
                writer.newLine();
                // ###########################

                writer.write("});");
                writer.newLine();

                System.out.println("Código escrito com sucesso!");
            }
        } catch (IOException e) {
            System.out.println("Error: " + e.getMessage());
        }
    }

